package com.Routing.Model;

public class RouterDetails {
	
	String startRouter=null;
	String endRouter=null;
	int distance=0;
	public String getStartRouter() {
		return startRouter;
	}
	public void setStartRouter(String startRouter) {
		this.startRouter = startRouter;
	}
	public String getEndRouter() {
		return endRouter;
	}
	public void setEndRouter(String endRouter) {
		this.endRouter = endRouter;
	}
	public int getDistance() {
		return distance;
	}
	public void setDistance(int distance) {
		this.distance = distance;
	}
	
	

}
